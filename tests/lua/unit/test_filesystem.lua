-- Luna2D Filesystem API Tests

describe("luna.fs module exists", function()
    it("luna.fs is a table", function()
        expect_type("table", luna.fs)
    end)
end)

describe("luna.fs basic operations", function()
    it("read is a function", function()
        expect_type("function", luna.fs.read)
    end)

    it("write is a function", function()
        expect_type("function", luna.fs.write)
    end)

    it("exists is a function", function()
        expect_type("function", luna.fs.exists)
    end)
end)

describe("luna.fs write and read", function()
    it("write creates a file", function()
        expect_no_error(function()
            luna.fs.write("save/_test_temp.txt", "hello luna2d")
        end)
    end)

    it("exists returns true for written file", function()
        luna.fs.write("save/_test_temp2.txt", "test")
        expect_true(luna.fs.exists("save/_test_temp2.txt"))
    end)

    it("read returns written content", function()
        luna.fs.write("save/_test_temp3.txt", "test content here")
        local content = luna.fs.read("save/_test_temp3.txt")
        expect_equal("test content here", content)
    end)

    it("exists returns false for non-existent file", function()
        expect_false(luna.fs.exists("save/_this_file_should_not_exist_xyz.txt"))
    end)

    it("write and read binary-safe", function()
        local data = "line1\nline2\nline3"
        luna.fs.write("save/_test_temp4.txt", data)
        local content = luna.fs.read("save/_test_temp4.txt")
        expect_equal(data, content)
    end)

    it("write overwrites existing file", function()
        luna.fs.write("save/_test_temp5.txt", "first")
        luna.fs.write("save/_test_temp5.txt", "second")
        local content = luna.fs.read("save/_test_temp5.txt")
        expect_equal("second", content)
    end)
end)

describe("filesystem.mount", function()
    it("mount() is a function", function()
        expect_type("function", luna.fs.mount)
    end)
    it("unmount() is a function", function()
        expect_type("function", luna.fs.unmount)
    end)
    it("load() is a function", function()
        expect_type("function", luna.fs.load)
    end)
    it("newFileData() is a function", function()
        expect_type("function", luna.fs.newFileData)
    end)
    it("load() compiles an existing Lua file", function()
        luna.fs.write("save/_test_chunk.lua", "return function() return 99 end")
        local f = luna.fs.load("save/_test_chunk.lua")
        expect_type("function", f)
        local inner = f()
        expect_type("function", inner)
        expect_equal(99, inner())
        luna.fs.remove("save/_test_chunk.lua")
    end)
end)

test_summary()
