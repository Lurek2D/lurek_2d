T.group("lurek.dsp bus effects", function()
    T.test("addEffectToBus returns a number effect id", function()
        lurek.audio.create_bus("test_dsp_bus")
        local eid = lurek.dsp.addEffectToBus("test_dsp_bus", "lowpass")
        T.assertEqual(type(eid), "number")
    end)
    T.test("removeEffectFromBus returns true", function()
        lurek.audio.create_bus("test_dsp_remove_bus")
        local eid = lurek.dsp.addEffectToBus("test_dsp_remove_bus", "lowpass")
        local ok = lurek.dsp.removeEffectFromBus("test_dsp_remove_bus", eid)
        T.assertEqual(ok, true)
    end)
    T.test("setEffectParam returns true for valid cutoff param", function()
        lurek.audio.create_bus("test_dsp_param_bus")
        local eid = lurek.dsp.addEffectToBus("test_dsp_param_bus", "lowpass")
        local ok = lurek.dsp.setEffectParam("test_dsp_param_bus", eid, "cutoff", 500.0)
        T.assertEqual(ok, true)
    end)
    T.test("addEffectToBus errors for unknown bus", function()
        local ok = pcall(function()
            lurek.dsp.addEffectToBus("nonexistent_bus_xyz_dsp", "lowpass")
        end)
        T.assertEqual(ok, false)
    end)
end)

