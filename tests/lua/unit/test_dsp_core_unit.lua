T.group("lurek.dsp.analyzeRms", function()
    T.test("returns a number in [0,1] for a sine wave", function()
        local sd = lurek.audio.newSineWave(440, 0.1, 44100, 0.5)
        local rms = lurek.dsp.analyzeRms(sd)
        T.assertEqual(type(rms), "number")
        T.assertEqual(rms >= 0.0, true)
        T.assertEqual(rms <= 1.0, true)
    end)
end)

T.group("lurek.dsp.analyzePeak", function()
    T.test("returns a number in [0,1] for a sine wave", function()
        local sd = lurek.audio.newSineWave(440, 0.1, 44100, 0.5)
        local peak = lurek.dsp.analyzePeak(sd)
        T.assertEqual(type(peak), "number")
        T.assertEqual(peak >= 0.0, true)
        T.assertEqual(peak <= 1.0, true)
    end)
    T.test("applyGain increases peak when gain > 1", function()
        local sd = lurek.audio.newSineWave(440, 0.1, 44100, 0.4)
        local before = lurek.dsp.analyzePeak(sd)
        lurek.dsp.applyGain(sd, 2.0)
        local after = lurek.dsp.analyzePeak(sd)
        T.assertEqual(after > before, true)
    end)
end)

T.group("lurek.dsp.analyzeFft", function()
    T.test("returns a table of bins with frequency and magnitude fields", function()
        local sd = lurek.audio.newSineWave(440, 0.1, 44100, 0.5)
        local bins = lurek.dsp.analyzeFft(sd, 16)
        T.assertEqual(type(bins), "table")
        T.assertEqual(#bins > 0, true)
        local first = bins[1]
        T.assertEqual(type(first.frequency), "number")
        T.assertEqual(type(first.magnitude), "number")
    end)
end)

T.group("lurek.dsp apply filters", function()
    T.test("applyLowpass does not error on LSoundData", function()
        local sd = lurek.audio.newSineWave(440, 0.1, 44100, 0.5)
