# ── Generic module API page ────────────────────────────────────────────────────

def gen_module_page(
    module: str,
    wiki_title: str,
    n_summary_paras: int = 2,
    example_max_lines: int = 90,
    extra_sections: str = "",
    see_also_override: list | None = None,
) -> str:
    spec = read_spec(module)
    if not spec:
        return (
            GEN_BANNER
            + f"# {wiki_title}\n\n"
            + f"> Specification not found for module `{module}`. "
            + "Check `specs/{module}.md`.\n"
        )

    tier    = spec.get("tier", "")
    status  = spec.get("status", "")
    api_ns  = spec.get("api",    f"luna.{module}")
    source  = spec.get("source", f"src/{module}/")
    tier_short = re.sub(r" — .*", "", tier)

    overview = _spec_summary(module, n_summary_paras)
    example  = read_example(module, example_max_lines)

    see_pages = see_also_override or _SEE_ALSO.get(module, [])
    see_links  = " · ".join(f"[[{p}]]" for p in see_pages)
    if see_links:
        see_links += " · "
    see_links += "[[API-Reference]]"

    parts = [
        GEN_BANNER,
        f"# {wiki_title}",
        "",
        f"> *{tier_short} · Lua API: `{api_ns}` · Source: `{source}` · {status}*",
        "",
        "---",
        "",
        "## Overview",
        "",
        overview,
        "",
    ]

    if example:
        parts += [
            "---",
            "",
            "## Example",
            "",
            "```lua",
            example,
            "```",
            "",
        ]

    if extra_sections:
        parts += [
            "---",
            "",
            extra_sections.strip(),
            "",
        ]

    parts += [
        "---",
        "",
        "## See Also",
        "",
        see_links,
    ]

    return "\n".join(parts)


