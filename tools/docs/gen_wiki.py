def select_pages(pages: list[Page], slug: str | None) -> list[Page]:
    if not slug:
        return pages
    filename = slug if slug.endswith(".md") else slug + ".md"
    selected = [page_item for page_item in pages if page_item.filename == filename]
    if not selected:
        raise SystemExit(f"Unknown page '{slug}'.")
    return selected


def check_pages(pages: list[Page], output: Path, page_filter: str | None) -> int:
    expected = {page_item.filename: page_item.content for page_item in pages}
    existing = {path.name for path in output.glob("*.md")} if output.exists() else set()
    failures: list[str] = []
    if page_filter is None:
        failures += ["missing " + name for name in sorted(set(expected) - existing)]
        failures += ["extra " + name for name in sorted(existing - set(expected))]
    for filename, expected_content in expected.items():
        path = output / filename
        if not path.exists():
            if page_filter is not None:
                failures.append("missing " + filename)
            continue
        actual = read(path)
        if actual != expected_content:
            failures.append("changed " + filename)
            diff = difflib.unified_diff(actual.splitlines(), expected_content.splitlines(), fromfile="existing/" + filename, tofile="generated/" + filename, lineterm="")
            for diff_line in list(diff)[:40]:
                print(diff_line)
    if failures:
        print("Wiki check failed:")
        for failure in failures:
            print("  " + failure)
        return 1
    print("Wiki check passed.")
    return 0


def remove_stale_generated_pages(pages: list[Page], output: Path, page_filter: str | None) -> None:
    if page_filter is not None or not output.exists():
        return
    expected = {page_item.filename for page_item in pages}
    for path in sorted(output.glob("*.md")):
        if path.name in expected:
            continue
        if read(path).startswith(BANNER.strip()):
            path.unlink()
            try:
                display = path.relative_to(ROOT).as_posix()
            except ValueError:
                display = path.as_posix()
            print(f"removed stale generated page {display}")


def validate_examples(context: Context) -> int:
    """Check that every callable in lua_api_data.json has a real example in the generated pages."""
    total = 0
    covered = 0
    failures: list[tuple[str, str]] = []
    for module, api_module in sorted(context.api_modules.items()):
        function_list = functions(api_module)
        class_list = classes(api_module)
        for function_data in function_list:
            total += 1
            example = callable_example(context, module, function_data)
            if example and example.snippet and _has_real_code(example.snippet):
                covered += 1
            else:
                name = str(function_data.get("lua_name", "") or function_data.get("name", ""))
                failures.append((module, name))
        for class_name, class_data in class_list:
            for method_data in methods(class_data):
                total += 1
                example = callable_example(context, module, method_data, class_name)
                if example and example.snippet and _has_real_code(example.snippet):
                    covered += 1
                else:
                    name = f"{class_name}:{method_data.get('name', '')}"
                    failures.append((module, name))
    pct = (covered * 100 // total) if total else 100
    print(f"Example coverage: {covered}/{total} callables ({pct}%)")
    if failures:
        print(f"\nMissing real examples ({len(failures)}):")
        for module, name in failures[:80]:
            print(f"  {module}: {name}")
        if len(failures) > 80:
            print(f"  ... and {len(failures) - 80} more")
    if pct < 100:
        print(f"\nValidation failed: coverage {pct}% < 100%")
        return 1
    print("Validation passed.")
    return 0


def _has_real_code(snippet: str) -> bool:
    """Return True if at least one line in the snippet is not a comment or blank."""
    for line in snippet.splitlines():
        stripped = line.strip()
        if stripped and not stripped.startswith("--"):
            return True
    return False


def run(argv: list[str]) -> int:
    parser = argparse.ArgumentParser(description="Generate Lurek2D GitHub Wiki pages.")
    parser.add_argument("--output", type=Path, default=DEFAULT_OUTPUT)
    parser.add_argument("--list", action="store_true")
    parser.add_argument("--page")
    parser.add_argument("--dry-run", action="store_true")
    parser.add_argument("--check", action="store_true")
    parser.add_argument("--validate", action="store_true")
    args = parser.parse_args(argv)
    output = args.output if args.output.is_absolute() else ROOT / args.output
    context = build_context(output.resolve())
    if args.validate:
        return validate_examples(context)
    pages = select_pages(build_pages(context), args.page)
    if args.list:
        for page_item in pages:
            print(page_item.slug)
        return 0
    if args.dry_run:
        for page_item in pages:
            print(f"{page_item.filename}\t{page_item.content.count(chr(10))} lines\t{len(page_item.content)} bytes")
        return 0
    if args.check:
        return check_pages(pages, output, args.page)
    output.mkdir(parents=True, exist_ok=True)
    remove_stale_generated_pages(pages, output, args.page)
    for page_item in pages:
        write(output / page_item.filename, page_item.content)
        try:
            display = output.relative_to(ROOT).as_posix()
        except ValueError:
            display = output.as_posix()
        print(f"wrote {display}/{page_item.filename}")
    return 0


if __name__ == "__main__":
    raise SystemExit(run(sys.argv[1:]))
