def key_text(value: Any) -> str:
    return re.sub(r"\s+", "", str(value or "").strip()).lower()


def callable_param_key(function_data: dict[str, Any]) -> tuple[Any, ...]:
    optional = optional_names(function_data)
    parts: list[tuple[str, bool]] = []
    for raw_param in function_data.get("typed_params", []) or []:
        if isinstance(raw_param, dict):
            name = str(raw_param.get("name", ""))
            raw_type = str(raw_param.get("type", raw_param.get("lua_type", "any")))
            is_optional = bool(raw_param.get("optional", False)) or name in optional or raw_type.endswith("?")
            parts.append((raw_type.rstrip("?").lower() or "any", is_optional))
        elif isinstance(raw_param, (list, tuple)) and len(raw_param) >= 2:
            name = str(raw_param[0])
            raw_type = str(raw_param[1])
            is_optional = name in optional or raw_type.endswith("?") or any(isinstance(extra, bool) and extra for extra in raw_param[2:])
            parts.append((raw_type.rstrip("?").lower() or "any", is_optional))
    if parts:
        return tuple(parts)

    inferred = str(function_data.get("inferred_sig", "") or "").strip()
    inferred = re.sub(r"\[[A-Za-z_][A-Za-z0-9_]*\]", "[]", inferred)
    inferred = re.sub(r"\b[A-Za-z_][A-Za-z0-9_]*\s*:", ":", inferred)
    return (key_text(inferred),) if inferred else ()


def callable_key(function_data: dict[str, Any]) -> tuple[Any, ...]:
    call_name = function_data.get("lua_name", "") or function_data.get("name", "")
    return (key_text(call_name), callable_param_key(function_data))


def callable_sort_key(function_data: dict[str, Any]) -> tuple[str, str, str]:
    return (
        str(function_data.get("name", "") or "").lower(),
        str(function_data.get("lua_name", "") or "").lower(),
        key_text(function_data.get("inferred_sig", "")),
    )


def dedupe_callables(items: list[dict[str, Any]]) -> list[dict[str, Any]]:
    result: list[dict[str, Any]] = []
    seen: set[tuple[Any, ...]] = set()
    for item in items:
        if not isinstance(item, dict):
            continue
        item_key = callable_key(item)
        if item_key in seen:
            continue
        seen.add(item_key)
        result.append(item)
    return result


def functions(api_module: dict[str, Any]) -> list[dict[str, Any]]:
    return sorted(dedupe_callables(api_module.get("functions", []) or []), key=callable_sort_key)


def classes(api_module: dict[str, Any]) -> list[tuple[str, dict[str, Any]]]:
    return sorted((api_module.get("classes", {}) or {}).items(), key=lambda item: item[0].lower())


def methods(class_data: dict[str, Any]) -> list[dict[str, Any]]:
    return sorted(dedupe_callables(class_data.get("methods", []) or []), key=callable_sort_key)


def api_callable_total(context: Context) -> int:
    total = 0
    for api_module in context.api_modules.values():
        total += len(functions(api_module))
        for _class_name, class_data in classes(api_module):
            total += len(methods(class_data))
    return total


def param_docs(function_data: dict[str, Any]) -> dict[str, str]:
    docs: dict[str, str] = {}
    for line in str(function_data.get("params_doc", "") or "").splitlines():
        clean_line = line.strip().lstrip("- ").strip()
        match = re.match(r"`?([A-Za-z_][A-Za-z0-9_]*)`?\s*(?:[:\-\u2013\u2014]|--)?\s*(.*)$", clean_line)
        if match:
            docs[match.group(1)] = match.group(2).strip()
    return docs


def optional_names(function_data: dict[str, Any]) -> set[str]:
    sig = str(function_data.get("inferred_sig", "") or "")
    return {match.group(1) for match in re.finditer(r"\[([A-Za-z_][A-Za-z0-9_]*)\]", sig)}


def params(function_data: dict[str, Any]) -> list[tuple[str, str, str, bool]]:
    docs = param_docs(function_data)
    optional = optional_names(function_data)
    result: list[tuple[str, str, str, bool]] = []
    for raw_param in function_data.get("typed_params", []) or []:
        if isinstance(raw_param, dict):
            name = str(raw_param.get("name", ""))
            type_name = str(raw_param.get("type", raw_param.get("lua_type", "any"))).rstrip("?") or "any"
            description = str(raw_param.get("description", docs.get(name, "")))
            is_optional = bool(raw_param.get("optional", False)) or name in optional
        elif isinstance(raw_param, (list, tuple)) and len(raw_param) >= 2:
            name = str(raw_param[0])
            type_name = str(raw_param[1]).rstrip("?") or "any"
            description = docs.get(name, "")
            is_optional = name in optional or str(raw_param[1]).endswith("?")
            for extra in raw_param[2:]:
                if isinstance(extra, bool):
                    is_optional = is_optional or extra
                elif isinstance(extra, str) and extra:
                    description = extra
        else:
            continue
        result.append((name, type_name, description, is_optional))
    return result


def return_type(function_data: dict[str, Any]) -> str:
    inferred = str(function_data.get("inferred_return", "") or "").strip()
    if inferred and inferred.lower() not in {"nil", "none", "()"}:
        return inferred
    returns_doc = str(function_data.get("returns_doc", "") or "").strip()
    if returns_doc and returns_doc.lower() not in {"nil", "none"}:
        match = re.match(r"`?([A-Za-z_][A-Za-z0-9_?\|]*)`?", returns_doc.splitlines()[0].strip())
        if match and match.group(1).lower() not in {"nil", "none", "the", "a", "an"}:
            return match.group(1).rstrip("?")
    return ""


def return_desc(function_data: dict[str, Any]) -> str:
    desc = str(function_data.get("return_description", "") or "").strip()
    if desc:
        return desc
    returns_doc = str(function_data.get("returns_doc", "") or "")
    return returns_doc.split(" - ", 1)[1].strip() if " - " in returns_doc else ""


def signature(function_data: dict[str, Any], module: str, class_name: str | None = None) -> str:
    param_parts = []
    for name, type_name, _description, is_optional in params(function_data):
        param_parts.append(f"{'[' + name + ']' if is_optional else name}: {type_name}")
    if not param_parts:
        inferred = str(function_data.get("inferred_sig", "") or "")
        if inferred.startswith("(") and inferred.endswith(")"):
            inferred_inner = inferred[1:-1].strip()
            if inferred_inner:
                param_parts.append(inferred_inner)
    name = str(function_data.get("name", ""))
    call = f"{class_name}:{name}" if class_name else str(function_data.get("lua_name", "") or f"lurek.{module}.{name}")
    result = f"{call}({', '.join(param_parts)})"
    returns = return_type(function_data)
    return f"{result} -> {returns}" if returns else result


def compact_line(function_data: dict[str, Any], module: str, class_name: str | None = None) -> str:
    desc = clean_text(str(function_data.get("description", "") or ""))
    if len(desc) > 110:
        desc = desc[:107].rstrip() + "..."
    return f"{signature(function_data, module, class_name)} -- {desc}" if desc else signature(function_data, module, class_name)


def callable_example(context: Context, module: str, function_data: dict[str, Any], class_name: str | None = None) -> ExampleSnippet | None:
    for key in callable_example_keys(module, function_data, class_name):
        if key in context.callable_examples:
            return context.callable_examples[key]
    if module in context.module_examples:
        return context.module_examples[module]
    return context.global_example


def rendered_example_section(context: Context, example: ExampleSnippet | None) -> list[str]:
    lines = ["#### Example", ""]
    if example is None or not example.snippet:
        lines += ["No generated example snippet was found in `content/examples/`.", "", "```lua", "-- No example snippet available.", "```", ""]
        return lines
    lines += [f"Source: {file_link(context, example.source_path, example.source_name)}", "", "```lua", example.snippet, "```", ""]
    return lines


def example_section(context: Context, module: str, function_data: dict[str, Any], class_name: str | None = None) -> list[str]:
    return rendered_example_section(context, callable_example(context, module, function_data, class_name))


def returns_type(function_data: dict[str, Any], class_name: str) -> bool:
    return class_name in {part.strip().rstrip("?") for part in return_type(function_data).split("|")}


def type_example(
    context: Context,
    module: str,
    class_name: str,
    function_list: list[dict[str, Any]],
    class_list: list[tuple[str, dict[str, Any]]],
) -> ExampleSnippet | None:
    for key in example_key_variants(class_name):
        if key in context.callable_examples:
            return context.callable_examples[key]
    for function_data in function_list:
        if returns_type(function_data, class_name):
            example = callable_example(context, module, function_data)
            if example and example.match_kind == "exact":
                return example
    for owner_name, owner_data in class_list:
        for method_data in methods(owner_data):
            if returns_type(method_data, class_name):
                example = callable_example(context, module, method_data, owner_name)
                if example and example.match_kind == "exact":
                    return example
    if module in context.module_examples:
        return context.module_examples[module]
    return context.global_example


def type_example_section(
    context: Context,
    module: str,
    class_name: str,
    function_list: list[dict[str, Any]],
    class_list: list[tuple[str, dict[str, Any]]],
) -> list[str]:
    return rendered_example_section(context, type_example(context, module, class_name, function_list, class_list))


def detail(context: Context, function_data: dict[str, Any], module: str, class_name: str | None = None, heading_level: int = 3) -> list[str]:
    fn_name = str(function_data.get("name", ""))
    if class_name:
        short_heading = f"{class_name}:{fn_name}"
    else:
        short_heading = str(function_data.get("lua_name", "") or f"lurek.{module}.{fn_name}")
    lines = [f"{'#' * heading_level} {short_heading}", ""]
    stub_keys = callable_example_keys(module, function_data, class_name)
    callable_stub = next((context.callable_stubs[example_key(k)] for k in stub_keys if example_key(k) in context.callable_stubs), "")
    definition_block = callable_stub or signature(function_data, module, class_name)
    lines += ["#### Definition", "", "```lua", definition_block, "```", ""]

    desc = clean_text(str(function_data.get("description", "") or ""))
    function_params = params(function_data)
    returns = return_type(function_data)
    returns_description = clean_text(return_desc(function_data))

    lines += ["#### Description", ""]
    if desc:
        lines += [desc, ""]
    if function_params:
        lines += ["Parameters:", ""]
        for name, type_name, description, is_optional in function_params:
            marker = "optional" if is_optional else "required"
            desc_text = clean_text(description)
            suffix = f": {desc_text}" if desc_text else ""
            lines.append(f"- `{name}` (`{type_name}`, {marker}){suffix}")
        lines.append("")
    if returns or returns_description:
        return_line = "Returns:"
        if returns:
            return_line += f" `{returns}`"
        if returns_description:
            return_line += f" - {returns_description}"
        lines += [return_line, ""]
    lines += example_section(context, module, function_data, class_name)
    return lines


def example_snippet(example: ExampleInfo, max_lines: int = 70) -> str:
    return example_snippet_from_lines(read(example.path).splitlines(), max_lines)


def related(context: Context, module: str, limit: int = 6) -> list[str]:
    group = module_group(context, module)
    return [candidate for candidate in context.modules if candidate != module and module_group(context, candidate) == group][:limit]


def module_neighbors(context: Context, module: str) -> tuple[str | None, str | None]:
    if module not in context.modules:
        return None, None
    index = context.modules.index(module)
    previous_module = context.modules[index - 1] if index > 0 else None
    next_module = context.modules[index + 1] if index + 1 < len(context.modules) else None
    return previous_module, next_module


