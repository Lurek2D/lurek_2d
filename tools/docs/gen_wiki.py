        callable_examples,
        module_examples,
        global_example,
        games,
        library_summary,
        library_entries,
        parse_callbacks(),
        modules,
        len(api_markdown_text.splitlines()) if api_markdown_text else 0,
        len(api_stub_text.splitlines()) if api_stub_text else 0,
