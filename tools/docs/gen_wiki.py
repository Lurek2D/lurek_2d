# CLI entry point
# ═══════════════════════════════════════════════════════════════════════════════

def main() -> int:
    parser = argparse.ArgumentParser(
        description="Regenerate all Luna2D wiki pages from source.",
    )
    parser.add_argument(
        "--page", metavar="SLUG",
        help="Regenerate a single page by slug (see --list for slugs)",
    )
    parser.add_argument(
        "--list", action="store_true",
        help="List all available page slugs and exit",
    )
    parser.add_argument(
        "--dry-run", action="store_true",
        help="Print generated content without writing files",
    )
    args = parser.parse_args()

    if args.list:
        for slug, (filename, _) in PAGES.items():
            print(f"  {slug:25s}  →  wiki/{filename}")
        return 0

    WIKI.mkdir(parents=True, exist_ok=True)

    if args.page:
        if args.page not in PAGES:
            print(f"ERROR: unknown page slug '{args.page}'. Run --list to see options.",
                  file=sys.stderr)
            return 1
        targets = {args.page: PAGES[args.page]}
    else:
        targets = PAGES

    errors = 0
    for slug, (filename, gen_fn) in targets.items():
        try:
            content = gen_fn()
            write_page(filename, content, dry_run=args.dry_run)
        except Exception as exc:  # noqa: BLE001
            print(f"  ERROR  {filename}: {exc}", file=sys.stderr)
            errors += 1

    if not args.dry_run:
        total = len(targets)
        print(f"\n{'✓' if errors == 0 else '!'} {total - errors}/{total} pages written to wiki/")
    return 0 if errors == 0 else 1


if __name__ == "__main__":
    sys.exit(main())
